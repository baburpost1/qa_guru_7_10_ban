class User():
    def __init__(self, first_name, last_name, email, gender, mobile, year, month, day, subjects, hobbies, picture,
                 address, state, city):
        self.first_name = first_name
        self.last_name = last_name
        self.email = email
        self.gender = gender
        self.mobile = mobile
        self.year = year
        self.month = month
        self.day = day
        self.subjects = subjects
        self.hobbies = hobbies
        self.picture = picture
        self.address = address
        self.state = state
        self.city = city
